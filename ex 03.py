n1 = int(input('digite um numero: '))
n2 = int(input('digite outro numero: '))

soma = n1+n2
subtracao = n1-n2
mult = n1*n2
div = n1/n2

print('{} + {} = {}'.format(n1, n2, soma))
print('{} - {} = {}'.format(n1, n2, subtracao))
print('{} * {} = {}'.format(n1, n2, mult))
print('{} / {} = {}'.format(n1, n2, div))
